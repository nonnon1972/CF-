
let year = 2023;

// 以下は全部、変数yearに1を足して再代入する処理！
//実際に出力をして確認するとより理解が深まるよ！

year += 1;
console.log(year)

year++;
console.log(year)

// 足す以外にも「割る」「引く」「掛ける」「余りを出す！」事ができるよ！
//実際に出力をして確認するとより理解が深まるよ！

year /= 1 
console.log(year)//yearを１で割ってyearに代入

year -= 1 
console.log(year);//yearから1を引いてyearに代入

year *= 1
console.log(year); //yearに１をかけるてyearに代入

year %= 10 
console.log(year)//yearを10で割って、その余りをyearに代入